# Comparison operators are used to compare two values:

x = 5
y = 3

print(x == y) #  ==	Equal	x == y	
print(x != y) #  !=	Not equal	x != y	
print(x > y) #  >	Greater than	x > y	
print(x < y) #  <	Less than	x < y	
print(x >= y) #  >=	Greater than or equal to	x >= y	
print(x <= y) #  <=	Less than or equal to	x <= y
