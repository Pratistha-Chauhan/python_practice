#TypeCasting is the process of converting one datatype to another.
#python provide the built-in function for typecasting.
 
#int(): convert into integer
#string(): convert into string
#float(): convert into float
#bool(): convert into boolean

a = 34 
b = "34"
d = 223


print(a)
print(type(a))

print(b)
print(type(b))

# Convert b to an integer
c = int(b)
print(c)
print(type(c))

e = str(d)
print(e)
print(type(e))

f = bool(e)
print(f)
print(type(f))

g = float(d)
print(g)
print(type(g))